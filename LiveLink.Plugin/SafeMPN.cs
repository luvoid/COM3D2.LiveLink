	{
		public static MPN ToMPN(this SafeMPN safempn)
		{
			return SafeEnumHelper<SafeMPN, MPN>.ToUnsafe(safempn);
		}
		public static SafeMPN ToSafeMPN(this MPN mpn)
		{
			return SafeEnumHelper<SafeMPN, MPN>.ToSafe(mpn, SafeMPN.null_mpn);
		}
	}
